import 'package:flutter/material.dart';

void main() {
  var a=10;
  var b=3;

  print(a+b);
  print(a-b);
  print(a*b);
  print(a/b);
  print(a%b);

  var x=10;
  print(x++);
  print(x++);
  print(++x);
  print(x);

  var y=10;
  print(y--);
  print(y--);
  print(--y);

  // Fixed Value
  const thisYear=2030;
  final thisYear2=2040;
  print(thisYear);
  print(thisYear2);

}